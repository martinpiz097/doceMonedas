    private void btnStartGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartGameActionPerformed
        final String name1 = txtPlayer1.getText().trim();
        final String name2 = txtPlayer2.getText().trim();
        
        if (name1.isEmpty() || name2.isEmpty()) 
            JOptionPane.showMessageDialog(this, "Debe ingresar el nombre de ambos jugadores");
        
        else{
            twelveCoins = new TwelveCoins(new Player(name1), new Player(name2, false));
            twelveCoins.builtCoins(btnSetTurn, lbl0, lbl1, lbl2, lbl3, lbl4, lbl5, 
                    lbl6, lbl7, lbl8, lbl9, lbl10, lbl11);
            twelveCoins.drawAll();
            hide();
            formGame.show();
            lblCurrentPlayer.setText(twelveCoins.getPlayerNameIsPlaying());
        }
    }//GEN-LAST:event_btnStartGameActionPerformed

