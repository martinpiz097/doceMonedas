    private void execCoinEvent(int index){
        final JLabel lbl;
        switch(index){
            case 0:
                lbl = lbl0;
                break;
                
            case 1:
                lbl = lbl1;
                break;
                
            case 2:
                lbl = lbl2;
                break;
                
            case 3:
                lbl = lbl3;
                break;
                
            case 4:
                lbl = lbl4;
                break;
                
            case 5:
                lbl = lbl5;
                break;
                
            case 6:
                lbl = lbl6;
                break;
                
            case 7:
                lbl = lbl7;
                break;
                
            case 8:
                lbl = lbl8;
                break;
                
            case 9:
                lbl = lbl9;
                break;
                
            case 10:
                lbl = lbl10;
                break;
                
            default:
                lbl = lbl11;
                break;
        }
        
        if (lbl.isVisible()){
            final int playsFollowed = twelveCoins.getPlaysFollowed();
            if (playsFollowed == 0) 
                btnSetTurn.setEnabled(true);
            
            if (playsFollowed < 2){
                twelveCoins.deleteCoin(index);
                testWinner();
                if (twelveCoins.getPlaysFollowed() == 2) setTurn();
            }
            else
                JOptionPane.showMessageDialog(this, "Ha alcanzado el límite de jugadas seguidas");
        }
    }
    
    private void testWinner(){
        if (twelveCoins.hasWinner()) {
            formGame.setEnabled(false);
            JOptionPane.showMessageDialog(formGame, "¡Juego terminado!\nHa ganado "
                    + twelveCoins.getPlayerNameWinner());
            System.exit(0);
        }
    }
    
    private void setTurn(){
        btnSetTurn.setEnabled(false);
        twelveCoins.setTurn();
        lblCurrentPlayer.setText(twelveCoins.getPlayerNameIsPlaying());
        testWinner();
    }
    
